#Atribuição
saldo = 500
print(saldo)

#Atribuição com adição
saldo += 200
print(saldo)

#Atribuição com subtração
saldo -= 200
print(saldo)

#Atribuição com multiplicação
saldo *= 2
print(saldo)

#Atribuição com divisão
saldo /= 2
print(saldo)

#Atribuição com divisão inteira
saldo //= 2
print(saldo)

#Atribuição com modulo
saldo %= 3
print(saldo)

#Atribuição com divisão Exponenciação
saldo **= 2
print(saldo)